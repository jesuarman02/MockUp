/* 
    | Nombre   | Sintaxis   |
    |----------|------------|
    |AND       | &&         |
    |OR        | ||         |
    |NOT       | !          |
*/
console.info("AND")
console.log(3 > 2 && 2 > 1)//true

console.info("OR")
console.log(3 > 2 || 2 > 1)//true
console.log(2 > 2 || 2 > 2)//falso

console.info("NOT")
console.log(!true)//falso
console.log(!false)//true




